    <p>
      <strong> TOWARD A MEDICINE THAT CARES</strong>
    </p>

    <p>
      <em>
        Helping people better manage their upsetting feelings —anger, anxiety,
        depression, pessimism, and loneliness —is a form of disease
        prevention.</em
      >
      Since the data show that the toxicity of these emotions, when chronic, is
      on a par with smoking cigarettes, helping people handle them better could
      potentially have a medical payoff as great as getting heavy smokers to
      quit.
    </p>

