      were found to have <strong>double the risk of disease</strong>—including
      asthma, arthritis, headaches, peptic ulcers, and heart disease (each
      representative of major, broad categories of disease). This order of
      magnitude makes distressing emotions as toxic a risk factor as, say,
      smoking or high cholesterol are for heart disease—in other words, a major
      threat to health. To be sure, this is a broad statistical link, and by no
      means indicates that everyone who has such chronic feelings will thus more
      easily fall prey to a disease. But the evidence for a potent role for
      emotion in disease is far more extensive than this one study of studies
      indicates.
    </p>

