    <p>
      In one of the most scientifically compelling studies, Sheldon Cohen, a
      psychologist at Carnegie-Mellon University, working with scientists at a
      specialized colds research unit in Sheffield, England, carefully assessed
      how much stress people were feeling in their lives, and then
      systematically exposed them to a cold virus. Not everyone so exposed
      actually comes down with a cold; a robust immune system can—and constantly
      does—resist the cold virus. Cohen found that the more stress in their
      lives, the more likely people were to catch cold. Among those with little
      stress, 27 percent came down with a cold after being exposed to the virus;
      among those with the most stressful lives, 47 percent got the cold—direct
      evidence that
      <strong> stress itself weakens the immune system.</strong>
      (While this may be one of those scientific results that confirms what
      everyone has observed or suspected all along, it is considered a landmark
      finding because of its scientific<TheTooltip
