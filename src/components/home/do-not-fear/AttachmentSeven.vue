    <p>
      Like its near cousin optimism, hope has healing power. People who have a
      great deal of hopefulness are, understandably, better able to bear up
      under trying circumstances, including medical difficulties. In a study of
      people paralysed from spinal injuries, those who had more hope were able
      to gain greater levels of physical mobility compared to other patients
      with similar degrees of injury, but who felt less hopeful. Hope is
      especially telling in paralysis from spinal injury, since this medical
      tragedy typically involves a man who is paralysed in his twenties by an
      accident and will remain so for the rest of his life. How he reacts
      emotionally will have broad consequences for the degree to which he will
      make the efforts that might bring him greater physical and social
      functioning. Just why an optimistic or pessimistic outlook should have
      health consequences is open to any of several explanations.
    </p>

    <p>
      One theory proposes that pessimism leads to depression, which in turn
      interferes with the resistance of the immune system to tumors and
      infection—an unproven speculation at present. Or it may be that pessimists
      neglect themselves—some studies have found that pessimists smoke and drink
      more, and exercise less, than optimists, and are generally much more
      careless about their health habits. Or it may one day turn out that
      the<TheTooltip
