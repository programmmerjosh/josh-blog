    <p>
      Anxiety—the distress<TheTooltip
        text="Evoke: bring or recall (a feeling, memory, or image) to the conscious mind."
        >&nbsp;evoked&nbsp;</TheTooltip
      >by life's pressures—is perhaps the emotion with the greatest weight of
      scientific evidence connecting it to the onset of sickness and course of
      recovery. When anxiety helps us prepare to deal with some danger (a
      presumed utility in evolution), then it has served us well. But in modern
      life anxiety is more often out of proportion and out of place—distress
      comes in the face of situations that we must live with or that are
      conjured by the mind, not real dangers we need to confront. Repeated bouts
      of anxiety signal high levels of stress. The woman whose constant worrying
      primes her gastrointestinal trouble is a textbook example of how anxiety
      and stress<TheTooltip
        text="Exacerbate: make (a problem, bad situation, or negative feeling) worse."
        >&nbsp;exacerbate&nbsp;</TheTooltip
      >medical problems.
    </p>

    <p class="">
      Particularly compelling evidence for the medical impact from distress has
      come from studies with infectious diseases such as colds, the flu, and
      herpes.
      <strong>
        We are continually exposed to such viruses, but ordinarily our immune
        system fights them off—except that under emotional stress those defenses
        more often fail.</strong
      >
    </p>

    <p>
      In experiments in which the<TheTooltip
        text="Robustness: the ability to withstand or overcome adverse conditions or rigorous testing."
