    <p class="d-inline">
      Historically, medicine in modern society has defined its mission in terms
      of curing disease —the medical disorder—while overlooking illness —the
      patient's experience of disease. Patients, by going along with this view
      of their problem, join a quiet conspiracy to ignore how they are reacting
      emotionally to their medical problems—or to dismiss those reactions as
      irrelevant to the course of the problem itself. That attitude is
      reinforced by a medical model that dismisses entirely the idea that mind
      influences body in any consequential way. Yet there is an equally
      unproductive ideology in the other direction: the notion that people can
      cure themselves of even the most<TheTooltip
        text="Pernicious: having a harmful effect, especially in a gradual or subtle way."
        >&nbsp;pernicious&nbsp;</TheTooltip
      >disease simply by making themselves happy or thinking positive thoughts,
      or that they are somehow to blame for having gotten sick in the first
      place. The result of this attitude-will-cure-all<TheTooltip
        text="Rhetoric: the art of effective or persuasive speaking or writing, especially the exploitation of figures of speech and other compositional techniques."
        >&nbsp;rhetoric&nbsp;</TheTooltip
      >has been to create widespread confusion and misunderstanding about the
      extent to which illness can be affected by the mind, and, perhaps worse,
      sometimes to make people feel guilty for having a disease, as though it
      were a sign of some moral lapse or spiritual unworthiness.
      <strong> The truth lies somewhere between these extremes.</strong>
    </p>

