    <p>
      Beyond such medical<TheTooltip
        text="Anecdote: an account regarded as unreliable or hearsay."
        >&nbsp;anecdotes</TheTooltip
      >, evidence for the clinical importance of emotions has been mounting
      steadily. Perhaps the most compelling data on the medical significance of
      emotion come from a mass analysis combining results from 101 smaller
      studies into a single larger one of several thousand men and women. The
      study confirms that<TheTooltip
        text="Perturbing: causing anxiety or concern; unsettling."
        >&nbsp;perturbing&nbsp;</TheTooltip
      >emotions are bad for health—to a degree. People who experienced chronic
      anxiety, long periods of sadness and pessimism,<TheTooltip
        text="Unremitting: never relaxing or slackening; incessant."
